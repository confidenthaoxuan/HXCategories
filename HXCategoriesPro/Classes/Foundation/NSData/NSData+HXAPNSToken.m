#import "NSData+HXAPNSToken.h"

@implementation NSData (HXAPNSToken)
- (NSString *)hx_APNSToken {
    return [[[[self description]
              stringByReplacingOccurrencesOfString: @"<" withString: @""]
             stringByReplacingOccurrencesOfString: @">" withString: @""]
            stringByReplacingOccurrencesOfString: @" " withString: @""];
}
@end
